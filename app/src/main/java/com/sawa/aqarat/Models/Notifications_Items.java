package com.sawa.aqarat.Models;


public class Notifications_Items {


    private String notify_Id;
    private String notify_Title;
    private String notify_Description;
    private String notify_is_Seen;
    private String notify_Redirect_ID;
    private String notify_Redirect_Type;



    public String getNotify_Id() {
        return notify_Id;
    }

    public void setNotify_Id(String notify_Id) {
        this.notify_Id = notify_Id;
    }


    public String getNotify_Title() {
        return notify_Title;
    }

    public void setNotify_Title(String notify_Title) {
        this.notify_Title = notify_Title;
    }


    public String getNotify_Description() {
        return notify_Description;
    }

    public void setNotify_Description(String notify_Description) {
        this.notify_Description = notify_Description;
    }

    public String getNotify_Redirect_ID() {
        return notify_Redirect_ID;
    }

    public void setNotify_Redirect_ID(String notify_Redirect_ID) {
        this.notify_Redirect_ID = notify_Redirect_ID;
    }


    public String getNotify_Redirect_Type() {
        return notify_Redirect_Type;
    }

    public void setNotify_Redirect_Type(String notify_Redirect_Type) {
        this.notify_Redirect_Type = notify_Redirect_Type;
    }


    public String getNotify_is_Seen() {
        return notify_is_Seen;
    }

    public void setNotify_is_Seen(String notify_is_Seen) {
        this.notify_is_Seen = notify_is_Seen;
    }

}




